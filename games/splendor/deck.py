from random import shuffle
from card import Card
from common import convert_cost


class Deck:
    tier = -1
    cards = []

    def __init__(self, tier):
        self.tier = tier
        self.prepare_cards()

    def prepare_cards(self):
        if self.tier < 0 or self.tier > 2:
            print("ERROR:  Cannot prepare tierless deck!")
            return
        self.cards = [self.make_card(stats) for stats in self.get_card_stats()[2 - self.tier]]
        shuffle(self.cards)

